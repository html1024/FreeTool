const AIImageDetectionTool: React.FC = () => {
    const [selectedFile, setSelectedFile] = useState<File | null>(null);
    const [previewUrl, setPreviewUrl] = useState('');
    const [result, setResult] = useState<DetectionResult | null>(null);
    const [isParsing, setIsParsing] = useState(false);
    const [isDragging, setIsDragging] = useState(false);
    const [error, setError] = useState<string | null>(null);
    const [copied, setCopied] = useState(false);
    const inputRef = useRef<HTMLInputElement>(null);

    useEffect(() => () => {
        if (previewUrl) URL.revokeObjectURL(previewUrl);
    }, [previewUrl]);

    const reset = useCallback(() => {
        if (previewUrl) URL.revokeObjectURL(previewUrl);
        setSelectedFile(null);
        setPreviewUrl('');
        setResult(null);
        setError(null);
        setCopied(false);
        if (inputRef.current) inputRef.current.value = '';
    }, [previewUrl]);

    const parseFile = useCallback(async (file: File) => {
        if (!file.type.startsWith('image/') && !/\.(jpe?g|png|webp|gif|avif|heic|heif|tiff?|bmp)$/i.test(file.name)) {
            setError('请选择图片文件');
            return;
        }

        if (previewUrl) URL.revokeObjectURL(previewUrl);

        const objectUrl = URL.createObjectURL(file);
        setSelectedFile(file);
        setPreviewUrl(objectUrl);
        setResult(null);
        setError(null);
        setCopied(false);
        setIsParsing(true);

        try {
            const arrayBuffer = await file.arrayBuffer();
            const bytes = new Uint8Array(arrayBuffer);
            const detectedType = detectImageType(bytes, file.type);

            const [dimensions, tags] = await Promise.all([
                loadImageDimensions(objectUrl),
                ExifReader.load(arrayBuffer, {
                    async: true,
                    computed: true,
                    expanded: true,
                    includeUnknown: true,
                }).catch((metadataError: unknown) => {
                    if (metadataError instanceof ExifReader.errors.MetadataMissingError) {
                        return {} as ExpandedTags;
                    }
                    throw metadataError;
                }),
            ]);

            const metadataRows = flattenMetadata(tags);
            const termHits = scanBinaryTerms(arrayBuffer);
            const fileInfo = {
                name: file.name,
                type: file.type,
                size: file.size,
                detectedType,
                lastModified: file.lastModified,
            };
            const baseResult = buildDetectionResult(file, dimensions, detectedType, metadataRows, termHits);
            const reportJson = buildReportJson(baseResult, fileInfo, dimensions);

            setResult({
                ...baseResult,
                file: fileInfo,
                dimensions,
                reportJson,
            });
        } catch (parseError) {
            console.error('AI image detection failed:', parseError);
            setError(parseError instanceof Error ? parseError.message : '图片检测失败');
        } finally {
            setIsParsing(false);
        }
    }, [previewUrl]);

    const handleFileSelect = useCallback((event: React.ChangeEvent<HTMLInputElement>) => {
        const file = event.target.files?.[0];
        if (file) parseFile(file);
    }, [parseFile]);

    const handleDrop = useCallback((event: React.DragEvent) => {
        event.preventDefault();
        setIsDragging(false);
        const file = event.dataTransfer.files?.[0];
        if (file) parseFile(file);
    }, [parseFile]);

    const handleCopy = useCallback(async () => {
        if (!result) return;
        await copyText(result.reportJson);
        setCopied(true);
        window.setTimeout(() => setCopied(false), 1800);
    }, [result]);

    const hitSummary = useMemo(() => {
        if (!result) return [];
        const labels = Array.from(new Set(result.termHits.map(hit => hit.label)));
        return ['C2PA', 'OpenAI', 'C2PA AI Source', 'AIGC', 'Nano-Banana-2', 'Doubao', 'Tencent Hunyuan', 'Ali Wanxiang']
            .map(label => ({
                label,
                active: labels.includes(label),
            }));
    }, [result]);

    const styles = result ? verdictStyle(result.verdict) : null;

    return (
        <div className="flex w-full flex-col items-center px-4 py-10 sm:px-6 lg:px-8">
            <div className="mb-8 flex w-full max-w-6xl flex-col items-center gap-2 text-center">
                <p className="text-3xl font-black leading-tight tracking-tighter text-gray-900 dark:text-white sm:text-4xl">
                    AI图片检测
                </p>
                <p className="text-base font-normal text-gray-500 dark:text-gray-400">
                    基于 C2PA、OpenAI、AIGC、XMP、EXIF 与 Author 标识判断图片来源
                </p>
            </div>

            <div className="flex w-full max-w-6xl flex-col gap-6">
                {error && (
                    <div className="rounded-lg border border-red-200 bg-red-100 p-4 dark:border-red-800 dark:bg-red-900/50">
                        <p className="text-sm text-red-800 dark:text-red-300">{error}</p>
                    </div>
                )}

                <section className="w-full overflow-hidden rounded-xl border border-gray-200 bg-white shadow-sm dark:border-gray-800 dark:bg-background-dark">
                    {!result && !isParsing ? (
                        <label
                            className={`m-6 flex cursor-pointer flex-col items-center gap-6 rounded-lg border-2 border-dashed p-8 text-center transition-colors sm:p-14 ${
                                isDragging
                                    ? 'border-primary bg-primary/5'
                                    : 'border-gray-300 hover:border-primary dark:border-gray-700 dark:hover:border-primary'
                            }`}
                            onDragEnter={(event) => {
                                event.preventDefault();
                                setIsDragging(true);
                            }}
                            onDragOver={(event) => event.preventDefault()}
                            onDragLeave={() => setIsDragging(false)}
                            onDrop={handleDrop}
                        >
                            <span className="material-symbols-outlined text-5xl text-gray-400 dark:text-gray-500">
                                policy
                            </span>
                            <div className="flex flex-col items-center gap-2">
                                <p className="text-lg font-bold text-gray-900 dark:text-white">拖拽图片至此</p>
                                <p className="text-sm text-gray-500 dark:text-gray-400">支持 JPG、PNG、WebP、GIF、AVIF、HEIC、TIFF</p>
                            </div>
                            <span className="flex h-10 min-w-[84px] cursor-pointer items-center justify-center overflow-hidden rounded-lg bg-gray-100 px-4 text-sm font-bold text-gray-800 transition-colors hover:bg-gray-200 dark:bg-white/10 dark:text-white dark:hover:bg-white/20">
                                点击选择文件
                            </span>
                            <input
                                ref={inputRef}
                                type="file"
                                accept="image/*,.heic,.heif,.avif,.tif,.tiff"
                                onChange={handleFileSelect}
                                className="hidden"
                            />
                        </label>
                    ) : (
                        <div className="grid grid-cols-1 lg:grid-cols-[minmax(0,0.9fr)_minmax(0,1.1fr)]">
                            <div className="relative flex flex-col gap-5 border-b border-gray-200 p-6 dark:border-gray-700/50 lg:border-b-0 lg:border-r">
                                <div className="flex items-center justify-between gap-3">
                                    <h3 className="flex items-center gap-2 text-base font-semibold leading-normal text-gray-900 dark:text-white">
                                        <span className="material-symbols-outlined text-xl">image</span>
                                        上传的图片
                                    </h3>
                                    <button
                                        onClick={reset}
                                        className="text-xs text-gray-500 hover:text-gray-700 dark:text-gray-400 dark:hover:text-gray-300"
                                    >
                                        更换图片
                                    </button>
                                </div>

                                <div className="flex min-h-[300px] flex-1 items-center justify-center rounded-lg border border-gray-200 bg-gray-50 p-4 dark:border-gray-700 dark:bg-gray-900/50">
                                    {previewUrl && !isParsing && (
                                        <img
                                            src={previewUrl}
                                            alt="Preview"
                                            className="max-h-[420px] max-w-full object-contain"
                                        />
                                    )}
                                    {isParsing && (
                                        <div className="flex flex-col items-center gap-3 text-gray-500 dark:text-gray-400">
                                            <div className="spinner" style={{ width: '32px', height: '32px', borderWidth: '3px', borderTopColor: '#607AFB' }}></div>
                                            <span className="text-sm">检测中...</span>
                                        </div>
                                    )}
                                </div>

                                {selectedFile && (
                                    <p className="truncate text-center text-xs text-gray-500 dark:text-gray-400">{selectedFile.name}</p>
                                )}

                                {result && (
                                    <div className="grid grid-cols-2 gap-3">
                                        <div className="rounded-lg border border-gray-200 bg-gray-50 p-3 dark:border-gray-700 dark:bg-gray-900/40">
                                            <p className="text-xs text-gray-500 dark:text-gray-400">文件大小</p>
                                            <p className="mt-1 text-sm font-semibold text-gray-900 dark:text-white">{formatBytes(result.file.size)}</p>
                                        </div>
                                        <div className="rounded-lg border border-gray-200 bg-gray-50 p-3 dark:border-gray-700 dark:bg-gray-900/40">
                                            <p className="text-xs text-gray-500 dark:text-gray-400">图片尺寸</p>
                                            <p className="mt-1 text-sm font-semibold text-gray-900 dark:text-white">
                                                {result.dimensions ? `${result.dimensions.width} x ${result.dimensions.height}` : '未读取'}
                                            </p>
                                        </div>
                                        <div className="rounded-lg border border-gray-200 bg-gray-50 p-3 dark:border-gray-700 dark:bg-gray-900/40">
                                            <p className="text-xs text-gray-500 dark:text-gray-400">识别格式</p>
                                            <p className="mt-1 text-sm font-semibold text-gray-900 dark:text-white">{result.file.detectedType}</p>
                                        </div>
                                        <div className="rounded-lg border border-gray-200 bg-gray-50 p-3 dark:border-gray-700 dark:bg-gray-900/40">
                                            <p className="text-xs text-gray-500 dark:text-gray-400">检测分数</p>
                                            <p className="mt-1 text-sm font-semibold text-gray-900 dark:text-white">{result.score}/100</p>
                                        </div>
                                    </div>
                                )}
                            </div>

                            <div className="flex min-h-[560px] flex-col gap-5 bg-gray-50/50 p-6 dark:bg-gray-800/30">
                                {result && styles ? (
                                    <>
                                        <div className={`rounded-lg border p-5 ${styles.panel}`}>
                                            <div className="flex items-start gap-3">
                                                <span className="material-symbols-outlined text-3xl">{styles.icon}</span>
                                                <div className="min-w-0 flex-1">
                                                    <p className="text-xl font-black leading-tight">{result.label}</p>
                                                    <p className="mt-2 text-sm leading-relaxed">{result.summary}</p>
                                                    <div className="mt-4 h-2 overflow-hidden rounded-full bg-white/70 dark:bg-black/20">
                                                        <div className={`h-full ${styles.bar}`} style={{ width: `${Math.max(6, result.score)}%` }}></div>
                                                    </div>
                                                </div>
                                            </div>
                                        </div>

                                        <div className="grid grid-cols-1 gap-3 sm:grid-cols-2">
                                            <div className="rounded-lg border border-gray-200 bg-white p-4 dark:border-gray-700 dark:bg-gray-900/40">
                                                <p className="text-xs text-gray-500 dark:text-gray-400">生成方判断</p>
                                                <p className="mt-1 text-base font-bold text-gray-900 dark:text-white">
                                                    {result.provider?.name ?? '暂未识别'}
                                                </p>
                                                <p className="mt-1 text-xs text-gray-500 dark:text-gray-400">
                                                    {result.provider ? `${result.provider.confidence}置信度 · ${result.provider.reason}` : '没有命中足够明确的厂商字符串'}
                                                </p>
                                            </div>
                                            <div className="rounded-lg border border-gray-200 bg-white p-4 dark:border-gray-700 dark:bg-gray-900/40">
                                                <p className="text-xs text-gray-500 dark:text-gray-400">修改时间</p>
                                                <p className="mt-1 text-base font-bold text-gray-900 dark:text-white">{formatDateTime(result.file.lastModified)}</p>
                                                <p className="mt-1 text-xs text-gray-500 dark:text-gray-400">未发现标识不等于图片一定由相机拍摄</p>
                                            </div>
                                        </div>

                                        <div className="grid grid-cols-2 gap-2 sm:grid-cols-4">
                                            {hitSummary.map(item => (
                                                <div
                                                    key={item.label}
                                                    className={`rounded-lg border px-3 py-2 text-xs font-semibold ${
                                                        item.active
                                                            ? 'border-primary/30 bg-primary/10 text-primary'
                                                            : 'border-gray-200 bg-white text-gray-400 dark:border-gray-700 dark:bg-gray-900/40 dark:text-gray-500'
                                                    }`}
                                                >
                                                    {item.label}
                                                </div>
                                            ))}
                                        </div>

                                        <div className="flex flex-col gap-3">
                                            <div className="flex items-center justify-between gap-3">
                                                <h3 className="text-base font-bold text-gray-900 dark:text-white">判定证据</h3>
                                                <button
                                                    onClick={handleCopy}
                                                    className="flex h-9 items-center justify-center gap-2 rounded-lg border border-gray-200 bg-white px-3 text-sm font-semibold text-gray-700 hover:bg-gray-50 dark:border-gray-700 dark:bg-gray-900 dark:text-gray-200 dark:hover:bg-gray-800"
                                                >
                                                    <span className="material-symbols-outlined text-lg">{copied ? 'check' : 'content_copy'}</span>
                                                    {copied ? '已复制' : '复制报告'}
                                                </button>
                                            </div>

                                            {result.evidence.length > 0 ? (
                                                <div className="space-y-3">
                                                    {result.evidence.map((item, index) => (
                                                        <div key={`${item.title}-${index}`} className="rounded-lg border border-gray-200 bg-white p-4 dark:border-gray-700 dark:bg-gray-900/40">
                                                            <div className="flex items-start gap-3">
                                                                <span className="material-symbols-outlined text-xl text-primary">{evidenceIcon[item.kind]}</span>
                                                                <div className="min-w-0 flex-1">
                                                                    <div className="flex flex-wrap items-center gap-2">
                                                                        <p className="text-sm font-bold text-gray-900 dark:text-white">{item.title}</p>
                                                                        <span className="rounded-full bg-gray-100 px-2 py-0.5 text-xs text-gray-500 dark:bg-gray-800 dark:text-gray-400">
                                                                            +{item.weight}
                                                                        </span>
                                                                    </div>
                                                                    <p className="mt-1 text-xs leading-relaxed text-gray-500 dark:text-gray-400">{item.detail}</p>
                                                                </div>
                                                            </div>
                                                        </div>
                                                    ))}
                                                </div>
                                            ) : (
                                                <div className="rounded-lg border border-gray-200 bg-white p-6 text-center dark:border-gray-700 dark:bg-gray-900/40">
                                                    <p className="text-sm text-gray-500 dark:text-gray-400">没有发现可用于判断 AI 来源的 metadata 证据</p>
                                                </div>
                                            )}
                                        </div>

                                        {result.termHits.length > 0 && (
                                            <details className="rounded-lg border border-gray-200 bg-white p-4 dark:border-gray-700 dark:bg-gray-900/40">
                                                <summary className="cursor-pointer text-sm font-bold text-gray-900 dark:text-white">字符串命中位置</summary>
                                                <div className="mt-3 grid grid-cols-1 gap-2 sm:grid-cols-2">
                                                    {result.termHits.map((hit, index) => (
                                                        <div key={`${hit.label}-${hit.term}-${index}`} className="rounded-lg bg-gray-50 p-3 dark:bg-gray-800/60">
                                                            <p className="text-xs font-bold text-gray-700 dark:text-gray-200">{hit.label} · {hit.term}</p>
                                                            <p className="mt-1 break-words text-xs text-gray-500 dark:text-gray-400">
                                                                {hit.offsets.map(offset => `0x${offset.toString(16)}`).join(', ')}
                                                            </p>
                                                        </div>
                                                    ))}
                                                </div>
                                            </details>
                                        )}
                                    </>
                                ) : (
                                    <div className="flex flex-1 items-center justify-center">
                                        <div className="flex flex-col items-center gap-3 text-gray-500 dark:text-gray-400">
                                            <div className="spinner" style={{ width: '32px', height: '32px', borderWidth: '3px', borderTopColor: '#607AFB' }}></div>
                                            <p className="text-sm">正在分析图片来源...</p>
                                        </div>
                                    </div>
                                )}
                            </div>
                        </div>
                    )}
                </section>
            </div>
        </div>
    );
};

export default AIImageDetectionTool;
