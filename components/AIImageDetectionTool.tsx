const MAX_OFFSETS = 6;
const MAX_VALUE_LENGTH = 3000;

const encoder = new TextEncoder();

const formatBytes = (bytes: number) => {
    if (bytes === 0) return '0 B';
    const units = ['B', 'KB', 'MB', 'GB'];
    const index = Math.min(Math.floor(Math.log(bytes) / Math.log(1024)), units.length - 1);
    const value = bytes / 1024 ** index;
    return `${value >= 100 ? Math.round(value) : value.toFixed(value >= 10 ? 1 : 2)} ${units[index]}`;
};

const formatDateTime = (time: number) => {
    if (!time) return '未知';
    return new Intl.DateTimeFormat('zh-CN', {
        year: 'numeric',
        month: '2-digit',
        day: '2-digit',
        hour: '2-digit',
        minute: '2-digit',
        second: '2-digit',
    }).format(new Date(time));
};

const readAscii = (bytes: Uint8Array, start: number, end: number) => {
    let output = '';
    for (let index = Math.max(0, start); index < Math.min(end, bytes.length); index += 1) {
        const code = bytes[index];
        output += code >= 32 && code <= 126 ? String.fromCharCode(code) : '.';
    }
    return output;
};

const detectImageType = (bytes: Uint8Array, fallbackType: string) => {
    if (bytes.length >= 12 && readAscii(bytes, 0, 4) === 'RIFF' && readAscii(bytes, 8, 12) === 'WEBP') return 'WebP';
    if (bytes.length >= 8 && bytes[0] === 0x89 && readAscii(bytes, 1, 4) === 'PNG') return 'PNG';
    if (bytes.length >= 3 && bytes[0] === 0xff && bytes[1] === 0xd8 && bytes[2] === 0xff) return 'JPEG';
    if (bytes.length >= 6 && (readAscii(bytes, 0, 6) === 'GIF87a' || readAscii(bytes, 0, 6) === 'GIF89a')) return 'GIF';
    if (bytes.length >= 12 && readAscii(bytes, 4, 8) === 'ftyp') return readAscii(bytes, 8, 12).trim() || 'ISO BMFF';
    if (bytes.length >= 2 && readAscii(bytes, 0, 2) === 'BM') return 'BMP';
    return fallbackType || '未知';
};

const findBytes = (bytes: Uint8Array, pattern: Uint8Array, maxCount = MAX_OFFSETS) => {
    const offsets: number[] = [];
    if (!pattern.length || pattern.length > bytes.length) return offsets;

    for (let index = 0; index <= bytes.length - pattern.length; index += 1) {
        let matched = true;
        for (let patternIndex = 0; patternIndex < pattern.length; patternIndex += 1) {
            if (bytes[index + patternIndex] !== pattern[patternIndex]) {
                matched = false;
                break;
            }
        }
        if (matched) {
            offsets.push(index);
            if (offsets.length >= maxCount) break;
        }
    }

    return offsets;
};

const scanBinaryTerms = (arrayBuffer: ArrayBuffer) => {
    const bytes = new Uint8Array(arrayBuffer);

    return TERM_GROUPS.flatMap(group => (
        group.terms
            .map(term => ({
                label: group.label,
                term,
                offsets: findBytes(bytes, encoder.encode(term)),
            }))
            .filter(hit => hit.offsets.length > 0)
    ));
};

const truncateValue = (value: string) => {
    if (value.length <= MAX_VALUE_LENGTH) return value;
    return `${value.slice(0, MAX_VALUE_LENGTH)}\n... 已截断 ${value.length - MAX_VALUE_LENGTH} 个字符`;
};

const safeStringify = (value: unknown) => {
    try {
        return JSON.stringify(value, (_key, item) => {
            if (item instanceof ArrayBuffer) return `[ArrayBuffer ${formatBytes(item.byteLength)}]`;
            if (ArrayBuffer.isView(item)) return `[${item.constructor.name} ${formatBytes(item.byteLength)}]`;
            if (typeof item === 'bigint') return item.toString();
            if (typeof item === 'string') return truncateValue(item);
            return item;
        }, 2);
    } catch {
        return String(value);
    }
};

const valueToText = (value: unknown): string => {
    if (value === null || value === undefined) return '';
    if (typeof value === 'string') return truncateValue(value);
    if (typeof value === 'number' || typeof value === 'boolean' || typeof value === 'bigint') return String(value);
    if (value instanceof ArrayBuffer) return `[ArrayBuffer ${formatBytes(value.byteLength)}]`;
    if (ArrayBuffer.isView(value)) return `[${value.constructor.name} ${formatBytes(value.byteLength)}]`;
    if (Array.isArray(value)) return safeStringify(value.length > 24 ? [...value.slice(0, 24), `... ${value.length - 24} more`] : value);
    return safeStringify(value);
};

const tagToText = (tag: unknown) => {
    if (!tag || typeof tag !== 'object') return valueToText(tag);
    const candidate = tag as { description?: unknown; value?: unknown };
    const description = valueToText(candidate.description);
    return description || valueToText(candidate.value ?? tag);
};

const flattenMetadata = (tags: ExpandedTags): MetadataRow[] => (
    Object.entries(tags).flatMap(([group, groupValue]) => {
        if (!groupValue || typeof groupValue !== 'object') return [];

        return Object.entries(groupValue as Record<string, unknown>)
            .map(([name, value]) => ({
                group,
                groupLabel: GROUP_LABELS[group] ?? group,
                name,
                value: tagToText(value),
            }))
            .filter(row => row.value.length > 0 && row.name.toLowerCase() !== 'image' && row.name.toLowerCase() !== 'base64');
    })
);

const loadImageDimensions = (url: string) => new Promise<ImageDimensions | null>((resolve) => {
    const image = new Image();
    image.onload = () => resolve({ width: image.naturalWidth, height: image.naturalHeight });
    image.onerror = () => resolve(null);
    image.src = url;
});

const rowContains = (row: MetadataRow, terms: string[]) => {
    const haystack = `${row.group} ${row.name} ${row.value}`.toLowerCase();
    return terms.some(term => haystack.includes(term.toLowerCase()));
};

const hasHit = (hits: TermHit[], label: string) => hits.some(hit => hit.label === label);

const hasAigcInGroup = (rows: MetadataRow[], group: string) => (
    rows.some(row => row.group.toLowerCase() === group && rowContains(row, ['AIGC', 'AI Generated', 'Generated by AI']))
);

const findAuthorAigcRow = (rows: MetadataRow[]) => rows.find(row => (
    /author|artist|creator|by-line|dc:creator|photoshop:authorsposition/i.test(row.name)
    && rowContains(row, ['AIGC'])
));

const findProviderRows = (rows: MetadataRow[], terms: string[]) => rows.filter(row => rowContains(row, terms));

const addEvidence = (
    evidence: EvidenceItem[],
    kind: EvidenceKind,
    title: string,
    detail: string,
    weight: number
) => {
    evidence.push({ kind, title, detail, weight });
};

const chooseProvider = (candidates: ProviderGuess[]) => {
    if (candidates.length === 0) return null;
    return [...candidates].sort((a, b) => b.score - a.score)[0];
};

const buildDetectionResult = (
    file: File,
    dimensions: ImageDimensions | null,
    detectedType: string,
    metadataRows: MetadataRow[],
    termHits: TermHit[]
): Omit<DetectionResult, 'file' | 'dimensions' | 'reportJson'> => {
    const evidence: EvidenceItem[] = [];
    const providers: ProviderGuess[] = [];
    const c2paFound = hasHit(termHits, 'C2PA');
    const openAiFound = hasHit(termHits, 'OpenAI') || findProviderRows(metadataRows, ['OpenAI', 'GPT-image', 'ChatGPT']).length > 0;
    const c2paAiSourceFound = hasHit(termHits, 'C2PA AI Source') || findProviderRows(metadataRows, ['trainedAlgorithmicMedia', 'digitalSourceType', 'createdWithAI']).length > 0;
    const nanoFound = hasHit(termHits, 'Nano-Banana-2') || findProviderRows(metadataRows, ['Nano Banana', 'Nano-Banana', 'Gemini', 'Google']).length > 0;
    const xmpAigcFound = hasAigcInGroup(metadataRows, 'xmp');
    const exifAigcFound = hasAigcInGroup(metadataRows, 'exif');
    const authorAigcRow = findAuthorAigcRow(metadataRows);
    const metadataAigcRows = metadataRows.filter(row => rowContains(row, ['AIGC', 'AI Generated', 'Generated by AI']));
    const binaryAigcFound = hasHit(termHits, 'AIGC');

    if (c2paFound && openAiFound) {
        addEvidence(evidence, 'provider', 'OpenAI C2PA Manifest', '检测到 C2PA/JUMBF 线索，并命中 OpenAI/GPT-image/ChatGPT 字符串。', 96);
        providers.push({
            name: 'OpenAI / GPT-image-2',
            confidence: '高',
            reason: 'C2PA Manifest 中出现 OpenAI 相关字符串',
            score: 96,
        });
    } else if (c2paFound && c2paAiSourceFound) {
        addEvidence(evidence, 'c2pa', 'C2PA AI 来源声明', '检测到 C2PA 结构，并命中 trainedAlgorithmicMedia / digitalSourceType 等 AI 来源字段。', 88);
    } else if (c2paFound) {
        addEvidence(evidence, 'c2pa', 'C2PA/JUMBF 线索', '图片中存在 C2PA 或 JUMBF 字符串。它可用于 AI 生成来源声明，也可能来自其他 provenance 工具。', 58);
    }

    if (c2paFound && nanoFound) {
        addEvidence(evidence, 'provider', 'Nano-Banana-2 / Google 系线索', '检测到 C2PA，同时命中 Nano Banana、Gemini 或 Google 相关字符串。', 82);
        providers.push({
            name: 'Nano-Banana-2 / Google 系生成器',
            confidence: '中',
            reason: 'C2PA 与 Nano Banana / Gemini / Google 字符串同时出现',
            score: 82,
        });
    }

    if (xmpAigcFound) {
        addEvidence(evidence, 'aigc', 'XMP AIGC 标识', 'XMP metadata 中出现 AIGC/AI Generated 字符串，符合豆包类图片的标记特征。', 88);
        providers.push({
            name: '豆包特征',
            confidence: '中',
            reason: 'AIGC 标识位于 XMP metadata',
            score: 78,
        });
    }

    if (exifAigcFound) {
        addEvidence(evidence, 'aigc', 'EXIF AIGC 标识', 'EXIF metadata 中出现 AIGC/AI Generated 字符串，符合腾讯混元类图片的标记特征。', 88);
        providers.push({
            name: '腾讯混元特征',
            confidence: '中',
            reason: 'AIGC 标识位于 EXIF metadata',
            score: 78,
        });
    }

    if (authorAigcRow) {
        addEvidence(evidence, 'aigc', 'Author=AIGC 标识', `${authorAigcRow.groupLabel}.${authorAigcRow.name} 字段中出现 AIGC，符合阿里万相类图片的标记特征。`, 92);
        providers.push({
            name: '阿里万相特征',
            confidence: '中',
            reason: 'Author/Creator 类属性中标记 AIGC',
            score: 84,
        });
    }

    if (!xmpAigcFound && !exifAigcFound && !authorAigcRow && metadataAigcRows.length > 0) {
        addEvidence(evidence, 'aigc', 'Metadata AIGC 字符串', `在 ${metadataAigcRows[0].groupLabel}.${metadataAigcRows[0].name} 等字段中发现 AIGC/AI Generated。`, 78);
    }

    if (binaryAigcFound && metadataAigcRows.length === 0) {
        addEvidence(evidence, 'aigc', '文件内 AIGC 字符串', '在图片二进制内容中发现 AIGC/AI Generated 字符串，但未能定位到结构化 metadata 字段。', 64);
    }

    const doubaoRows = findProviderRows(metadataRows, ['Doubao', '豆包', 'ByteDance', 'Seedream']);
    if (doubaoRows.length > 0 && (metadataAigcRows.length > 0 || binaryAigcFound)) {
        providers.push({
            name: '豆包',
            confidence: '高',
            reason: 'AIGC 标识与豆包/字节相关字符串同时出现',
            score: 90,
        });
    }

    const hunyuanRows = findProviderRows(metadataRows, ['Hunyuan', '混元', 'Tencent', '腾讯']);
    if (hunyuanRows.length > 0 && (metadataAigcRows.length > 0 || binaryAigcFound)) {
        providers.push({
            name: '腾讯混元',
            confidence: '高',
            reason: 'AIGC 标识与混元/腾讯相关字符串同时出现',
            score: 90,
        });
    }

    const wanxiangRows = findProviderRows(metadataRows, ['Wanxiang', '万相', 'Tongyi', '通义', 'Alibaba', '阿里']);
    if (wanxiangRows.length > 0 && (metadataAigcRows.length > 0 || binaryAigcFound || authorAigcRow)) {
        providers.push({
            name: '阿里万相',
            confidence: '高',
            reason: 'AIGC 标识与万相/通义/阿里相关字符串同时出现',
            score: 90,
        });
    }

    const topEvidence = evidence.reduce((max, item) => Math.max(max, item.weight), 0);
    const bonus = Math.min(10, Math.max(0, evidence.length - 1) * 4);
    const score = Math.min(99, topEvidence + bonus);
    const verdict: Verdict = score >= 75 ? 'ai' : score >= 45 ? 'suspicious' : 'none';
    const provider = chooseProvider(providers);

    if (verdict === 'ai') {
        return {
            verdict,
            score,
            label: '检测到 AI 生成标识',
            summary: provider ? `图片内嵌标识显示它很可能由 ${provider.name} 生成。` : '图片内嵌 metadata / 字符串包含明确的 AI 生成线索。',
            provider,
            evidence,
            termHits,
            metadataRows,
        };
    }

    if (verdict === 'suspicious') {
        return {
            verdict,
            score,
            label: '疑似 AI 生成',
            summary: '发现了 provenance 或 AIGC 相关线索，但不足以稳定判定具体生成方。',
            provider,
            evidence,
            termHits,
            metadataRows,
        };
    }

    return {
        verdict,
        score,
        label: '未发现 AI 生成标识',
        summary: '没有在 metadata 或文件内容中发现 C2PA/OpenAI/AIGC 等可识别线索。',
        provider,
        evidence,
        termHits,
        metadataRows,
    };
};

const buildReportJson = (
    result: Omit<DetectionResult, 'file' | 'dimensions' | 'reportJson'>,
    file: DetectionResult['file'],
    dimensions: ImageDimensions | null
) => JSON.stringify({
    verdict: result.verdict,
    score: result.score,
    label: result.label,
    summary: result.summary,
    provider: result.provider,
    evidence: result.evidence,
    termHits: result.termHits.map(hit => ({
        ...hit,
        offsets: hit.offsets.map(offset => `0x${offset.toString(16)}`),
    })),
    file: {
        ...file,
        sizeText: formatBytes(file.size),
        lastModifiedText: formatDateTime(file.lastModified),
    },
    dimensions,
}, null, 2);

const copyText = async (value: string) => {
    if (navigator.clipboard?.writeText) {
        await navigator.clipboard.writeText(value);
        return;
    }

    const textarea = document.createElement('textarea');
    textarea.value = value;
    textarea.setAttribute('readonly', 'true');
    textarea.style.position = 'absolute';
    textarea.style.left = '-9999px';
    document.body.appendChild(textarea);
    textarea.select();
    document.execCommand('copy');
    document.body.removeChild(textarea);
};

const verdictStyle = (verdict: Verdict) => {
    if (verdict === 'ai') {
        return {
            panel: 'border-rose-200 bg-rose-50 text-rose-900 dark:border-rose-900/60 dark:bg-rose-950/30 dark:text-rose-200',
            bar: 'bg-rose-500',
            icon: 'policy',
        };
    }
    if (verdict === 'suspicious') {
        return {
            panel: 'border-amber-200 bg-amber-50 text-amber-900 dark:border-amber-900/60 dark:bg-amber-950/30 dark:text-amber-200',
            bar: 'bg-amber-500',
            icon: 'manage_search',
        };
    }
    return {
        panel: 'border-emerald-200 bg-emerald-50 text-emerald-900 dark:border-emerald-900/60 dark:bg-emerald-950/30 dark:text-emerald-200',
        bar: 'bg-emerald-500',
        icon: 'check_circle',
    };
};

